# User Stories

#     As a user, I can add a snippet to the data store, so that I can look it up again when I need it
#     As a user, I can request a snippet by its ID, so that I can see the snippet again

# Functional Requirements

#     Users can make POST request to /snippet to create a new snippet
#     Users can make a GET request to /snippet to get all the snippets currently in the data store
#     Users can make a GET request to e.g. /snippet/3 to retrieve the snippet with the ID of 3
#     Bonus: Users can make a GET request to e.g. /snippet?lang=python to retrieve all the Python snippets

